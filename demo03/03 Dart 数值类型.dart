void main() {
  // int
  int count = 19;

  // double 
  double price = 99.99;
  double sales_prices = 89;

  // 运算符
  // + 
  print(count + sales_prices);

  // -
  print(sales_prices - count);

  // *
  print(count * price);
  
  // /
  print(sales_prices / count);
  
  // %
  print(price % count);

}
